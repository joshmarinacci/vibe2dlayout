export interface EmojiMatch {
    name: string
    char: string
}

export function searchEmojis(query: string): EmojiMatch[] {
    if (!query) return []
    const q = query.toLowerCase()
    const prefix: EmojiMatch[] = []
    const contains: EmojiMatch[] = []
    for (const [name, char] of EMOJI_DATA) {
        if (name.startsWith(q)) prefix.push({name, char})
        else if (name.includes(q)) contains.push({name, char})
    }
    // Within each group: shorter names first (more specific match), then alphabetical
    const byLength = (a: EmojiMatch, b: EmojiMatch) =>
        a.name.length - b.name.length || a.name.localeCompare(b.name)
    return [...prefix.sort(byLength), ...contains.sort(byLength)].slice(0, 20)
}
